package biganearjava;

public class Bitwise_operator_2 {
    
    public static void main(String[] args) {
        
        int x= 64;
        int y =128;
        
        int ab=x>>2;//64/2=32/2=16
        System.out.println("ab =" +ab);
        
        ab=y<<2; //128*2=256*2=512
        System.out.println("ab =" +ab);
        
         
    }
}
