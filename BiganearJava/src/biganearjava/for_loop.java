package biganearjava;

public class for_loop {

    public static void main(String[] args) {
        
        for(int A=1; A<=20; A++){
            System.out.println(A+".Awsome");
        }//normal Incremant loop
        
        
        System.out.println("00000000000000000000000000");
        
        
        for(int A=1; A<=20; A=A+2){
            System.out.println(A+".Awsome");
        }//1 pore pore 2 pore pore Incremant loop ODD NUMBER
        
        
        System.out.println("00000000000000000000000000");
        
        
        for(int A=2; A<=20; A=A+2){
            System.out.println(A+".Awsome");
        }//1 pore pore 2 pore pore Incremant loop Even NUMBER
        
        
        System.out.println("00000000000000000000000000");
        
        
        for(int A=20; A>=1; A--){
            System.out.println(A+".Awsome");
        }//Normal Decrimant Loop
        
        
        System.out.println("00000000000000000000000000");
        
        
        for(int A=19; A>=1; A=A-2){
            System.out.println(A+".Awsome");
        }//1 pore pore 2 pore pore Decrimant Loop ODD NUMBER+ initioiyalijationo o 1 kom nibo
        
        
        System.out.println("00000000000000000000000000");
        
        
         for(int A=20; A>=1; A=A-2){
            System.out.println(A+".Awsome");
        }//1 pore pore 2 pore pore Decrimant Loop EVEN NUMBER
    }
}
