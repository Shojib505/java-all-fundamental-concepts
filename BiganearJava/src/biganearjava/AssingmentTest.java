
package biganearjava;

public class AssingmentTest {
    
    public static void main(String[] args){
        
        int x=10;
        int y=5;
        
        x+=y;//x=x+y=15       
        System.out.println("X = "+x);
        
        x-=y;//x=x+y=10
        System.out.println("X = "+x);
        
        x*=y;//x=x+y=50
        System.out.println("X = "+x);
 
        x/=y;//x=x+y=10
        System.out.println("X = "+x);
        
        x%=y;//x=x+y=0
        System.out.println("X = "+x);
        
      
    }
    
}
