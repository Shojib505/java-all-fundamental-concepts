package math_class;

public class math {
    public static void main(String[] args) {
        System.out.println("Math abs is : "+Math.abs(-45.6));
        System.out.println("Math sqrt is : "+Math.sqrt(10));
        System.out.println("Math 2 power 3 is  : "+Math.pow(2, 3));
        double p = Math.PI;
        System.out.println("Math PI is : "+p);
        System.out.println("Math log is : "+Math.log(10));
        System.out.println("Math exp is : "+Math.exp(20));
        System.out.println("Math maximum exp is : "+Math.max(10,-20));
        System.out.println("Math minimunm exp is : "+Math.min(20,-30));
        System.out.println("Math ceiling exp is : "+Math.ceil(5.6));
        System.out.println("Math floor exp is : "+Math.floor(-5.5));
        
    }
}
